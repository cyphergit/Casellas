            <h3>Gourmet Godfather Top 50 Restaurant Guide 2010</h3>
            <div>
                <p>
                    <strong>Casellas is a breath of fresh air in the south west when it comes to what it offers to the public.
                        Set on the south side of Koombana Bay, Casellas is in the hub of the new developments that have
                        transformed the inner beach suburbs in Bunbury.</strong>
                </p>
                <p>
                    A modern bar offers chic style with leather couches available inside and comfortable seating in the alfresco area.
                    There are nearly 400 wines on the list and the onerous task of selection is made easier with the well informed drink staff.
                </p>
                <p>
                    The menu is in line with the up to date presentation of the location. Tapas are available together with an a-la-carte menu or you
                    can go in and enjoy a glass of your favourite wine or beer. The food served is modern, well presented and tasty. It can
                    be served to you on the wood topped tables inside with a seating capacity of about 80 and well over 100 other
                    people can be served on the outdoor facilities.
                </p>
                <p>
                    The tapas range from $8.00 to $17.00, the heartier dishes are at the $30.00 mark.
                </p>
                <p>
                    An average three course meal will cost around $60.
                </p>
            </div>

